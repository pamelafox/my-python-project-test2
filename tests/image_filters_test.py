from image_filters import remove_red


def test_remove_red():
    assert remove_red([[[0, 0, 0], [255, 0, 0]], [[0, 255, 0], [0, 0, 255]]]) == [
        [[0, 0, 0], [0, 0, 0]],
        [[0, 255, 0], [0, 0, 255]],
    ]
    assert remove_red([[[0, 0, 0], [100, 0, 0]], [[150, 255, 0], [255, 0, 255]]]) == [
        [[0, 0, 0], [0, 0, 0]],
        [[0, 255, 0], [0, 0, 255]],
    ]
